#include <stdio.h>

int ft_atoi(char *str)
{
	int i;
	int signal;
	int num;

	i = 0;
	signal = 1;
	num = 0;
	while (str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signal *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num *= 10;
		num += str[i++] - '0';
	}
	return (signal * num);
}

int main(void)
{
	printf("%d\n", ft_atoi("   +12"));
}